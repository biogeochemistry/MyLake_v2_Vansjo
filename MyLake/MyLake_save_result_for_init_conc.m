function MyLake_save_result_for_init_conc(MyLake_results, n)

if n == 1
    file_name = 'IO/mylake_initial_concentrations.txt';
else
    file_name = 'IO/mylake_initial_concentrations_2.txt';
end

fid = fopen(file_name,'wt');
fprintf(fid, 'skip \n Z (m)\t  Az (m2)\t Tz (deg C)\t  Cz(mg/m3)\t  POCz (mg/m3)\t  TPz (mg/m3)\t DOPz (mg/m3)\t    Chlaz (mg/m3)\t   DOCz (mg/m3)\t    TPz_sed (mg/m3)\t Chlaz_sed (mg/m3)\t   Fvol_IM (m3/m3, dry w.)\t Hice (m)\t Hsnow (m)\t O2z (mg/m3)\t DICz (mg/m3)\t NO3z (mg/m3)\t NH4z (mg/m3)\t SO4z (mg/m3)\t HSz (mg/m3)\t H2Sz (mg/m3)\t Fe2z (mg/m3)\t Ca2z (mg/m3)\t pHz (mg/m3)\t CH4z (mg/m3)\t Fe3z (mg/m3)\t Al3z (mg/m3)\t SiO4z (mg/m3)\t SiO2z (mg/m3)\t diatomz (mg/m3)\t POPz (mg/m3)\n');
fclose(fid);


dlmwrite(file_name, ...
 [MyLake_results.z(:,end), ...
 MyLake_results.params.Az(:,end), ...
 MyLake_results.T(:,end), ...
 MyLake_results.concentrations.C(:,end), ...
 MyLake_results.concentrations.POC(:,end), ...
 MyLake_results.concentrations.P(:,end)+MyLake_results.concentrations.PP(:,end)+MyLake_results.concentrations.DOP(:,end)+MyLake_results.concentrations.Chl(:,end)+MyLake_results.concentrations.C(:,end), ...
 MyLake_results.concentrations.DOP(:,end), ...
 MyLake_results.concentrations.Chl(:,end), ...
 MyLake_results.concentrations.DOC(:,end), ...
 zeros(length(MyLake_results.z),1), ...  % In_TPz_sed
 zeros(length(MyLake_results.z),1), ...  % In_Chlz_sed
 zeros(length(MyLake_results.z),1), ...   % MyLake_results.FIM(:,end)
 zeros(length(MyLake_results.z),2), ...  % ice0
 MyLake_results.concentrations.O2(:,end), ...
 MyLake_results.concentrations.DIC(:,end), ...
 MyLake_results.concentrations.NO3(:,end), ...
 MyLake_results.concentrations.NH4(:,end), ...
 MyLake_results.concentrations.SO4(:,end), ...
 MyLake_results.concentrations.HS(:,end), ...
 MyLake_results.concentrations.H2S(:,end), ...
 MyLake_results.concentrations.Fe2(:,end), ...
 MyLake_results.concentrations.Ca2(:,end), ...
 MyLake_results.concentrations.pH(:,end), ...
 MyLake_results.concentrations.CH4(:,end), ...
 MyLake_results.concentrations.Fe3(:,end), ...
 MyLake_results.concentrations.Al3(:,end), ...
 MyLake_results.concentrations.SiO4(:,end), ...
 MyLake_results.concentrations.SiO2(:,end), ...
 MyLake_results.concentrations.diatom(:,end), ...
 MyLake_results.concentrations.POP(:,end)], 'delimiter', '\t', '-append')


dlmwrite(file_name, ...
 [MyLake_results.z(end,end) + MyLake_results.z(end,end) - MyLake_results.z(end-1,end), ...
 0, ...
 MyLake_results.T(end,end), ...
 MyLake_results.concentrations.C(end,end), ...
 MyLake_results.concentrations.POC(end,end), ...
 MyLake_results.concentrations.P(end,end)+MyLake_results.concentrations.PP(end,end)+MyLake_results.concentrations.DOP(end,end)+MyLake_results.concentrations.Chl(end,end)+MyLake_results.concentrations.C(end,end), ...
 MyLake_results.concentrations.DOP(end,end), ...
 MyLake_results.concentrations.Chl(end,end), ...
 MyLake_results.concentrations.DOC(end,end), ...
 0, ...  % In_TPz_sed
 0, ...  % In_Chlz_sed
 0, ...   % MyLake_results.FIM(end,end)
 0, ...  % ice0
 0, ...  % ice0
 MyLake_results.concentrations.O2(end,end), ...
 MyLake_results.concentrations.DIC(end,end), ...
 MyLake_results.concentrations.NO3(end,end), ...
 MyLake_results.concentrations.NH4(end,end), ...
 MyLake_results.concentrations.SO4(end,end), ...
 MyLake_results.concentrations.HS(end,end), ...
 MyLake_results.concentrations.H2S(end,end), ...
 MyLake_results.concentrations.Fe2(end,end), ...
 MyLake_results.concentrations.Ca2(end,end), ...
 MyLake_results.concentrations.pH(end,end), ...
 MyLake_results.concentrations.CH4(end,end), ...
 MyLake_results.concentrations.Fe3(end,end), ...
 MyLake_results.concentrations.Al3(end,end), ...
 MyLake_results.concentrations.SiO4(end,end), ...
 MyLake_results.concentrations.SiO2(end,end), ...
 MyLake_results.concentrations.diatom(end,end), ...
 MyLake_results.concentrations.POP(end,end)], 'delimiter', '\t', '-append')

