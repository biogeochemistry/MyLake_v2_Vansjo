 MyLake_results.DOPzt(:,end), ...
 MyLake_results.Chlzt(:,end), ...
 MyLake_results.DOCzt(:,end), ...
 zeros(length(MyLake_results.z),1), ...  % In_TPz_sed
 zeros(length(MyLake_results.z),1), ...  % In_Chlz_sed
 zeros(length(MyLake_results.z),1), ...   % MyLake_results.FIM(:,end)
 zeros(length(MyLake_results.z),2), ...  % ice0
 MyLake_results.O2zt(:,end), ...
 MyLake_results.DICzt(:,end), ...
 MyLake_results.NO3zt(:,end), ...
 MyLake_results.NH4zt(:,end), ...
 MyLake_results.SO4zt(:,end), ...
 MyLake_results.HSzt(:,end), ...
 MyLake_results.H2Szt(:,end), ...
 MyLake_results.Fe2zt(:,end), ...
 MyLake_results.Ca2zt(:,end), ...
 MyLake_results.pHzt(:,end), ...
 MyLake_results.CH4zt(:,end), ...
 MyLake_results.Fe3zt(:,end), ...
 MyLake_results.Al3zt(:,end), ...
 MyLake_results.SiO4zt(:,end), ...
 MyLake_results.SiO2zt(:,end), ...
 MyLake_results.diatomzt(:,end), ...
 MyLake_results.POCzt(:,end)], 'delimiter', '\t', '-append')


dlmwrite(file_name, ...
 [MyLake_results.z(end,end) + MyLake_results.z(end,end) - MyLake_results.z(end-1,end), ...
 0, ...
 MyLake_results.Tzt(end,end), ...
 MyLake_results.Czt(end,end), ...
 MyLake_results.Szt(end,end), ...
 MyLake_results.Pzt(end,end)+MyLake_results.PPzt(end,end)+MyLake_results.DOPzt(end,end)+MyLake_results.Chlzt(end,end)+MyLake_results.Czt(end,end), ...
 MyLake_results.DOPzt(end,end), ...
 MyLake_results.Chlzt(end,end), ...
 MyLake_results.DOCzt(end,end), ...
 0, ...  % In_TPz_sed
 0, ...  % In_Chlz_sed
 0, ...   % MyLake_results.FIM(end,end)
 0, ...  % ice0
 0, ...  % ice0
 MyLake_results.O2zt(end,end), ...
 MyLake_results.DICzt(end,end), ...
 MyLake_results.NO3zt(end,end), ...
 MyLake_results.NH4zt(end,end), ...
 MyLake_results.SO4zt(end,end), ...
 MyLake_results.HSzt(end,end), ...
 MyLake_results.H2Szt(end,end), ...
 MyLake_results.Fe2zt(end,end), ...
 MyLake_results.Ca2zt(end,end), ...
 MyLake_results.pHzt(end,end), ...
 MyLake_results.CH4zt(end,end), ...
 MyLake_results.Fe3zt(end,end), ...
 MyLake_results.Al3zt(end,end), ...
 MyLake_results.SiO4zt(end,end), ...
