function sediment_save_result_for_init_conc(sed_res, n)

if n == 1
    file_name = 'IO/sediment_initial_concentrations.txt'
else
    file_name = 'IO/sediment_initial_concentrations_2.txt'
end

fid = fopen(file_name,'wt');
fprintf(fid, 'z\t OMzt\t OMbzt\t DOM1zt\t DOM2zt\t O2zt\t NO3zt\t NH4zt\t NH3zt\t FeOH3zt\t FeOOHzt\t Fe2zt\t SO4zt\t H2Szt\t HSzt\t PO4zt\t PO4adsazt\t PO4adsbzt\t S0zt\t S8zt\t FeSzt\t FeS2zt\t AlOH3zt\t Ca2zt\t Ca3PO42zt\t OMSzt\t Hzt\t OHzt\t CO2zt\t CO3zt\t HCO3zt\t H2CO3zt\n');
fclose(fid);

dlmwrite(file_name, ...
[sed_res.params.x', ...
sed_res.OMzt(:,end), ...
sed_res.OMbzt(:,end), ...
sed_res.DOM1zt(:,end), ...
sed_res.DOM2zt(:,end), ...
sed_res.O2zt(:,end), ...
sed_res.NO3zt(:,end), ...
sed_res.NH4zt(:,end), ...
sed_res.NH3zt(:,end), ...
sed_res.FeOH3zt(:,end), ...
sed_res.FeOOHzt(:,end), ...
sed_res.Fe2zt(:,end), ...
sed_res.SO4zt(:,end), ...
sed_res.H2Szt(:,end), ...
sed_res.HSzt(:,end), ...
sed_res.PO4zt(:,end), ...
sed_res.PO4adsazt(:,end), ...
sed_res.PO4adsbzt(:,end), ...
sed_res.S0zt(:,end), ...
sed_res.S8zt(:,end), ...
sed_res.FeSzt(:,end), ...
sed_res.FeS2zt(:,end), ...
sed_res.AlOH3zt(:,end), ...
sed_res.Ca2zt(:,end), ...
sed_res.Ca3PO42zt(:,end), ...
sed_res.OMSzt(:,end), ...
sed_res.Hzt(:,end), ...
sed_res.OHzt(:,end), ...
sed_res.CO2zt(:,end), ...
sed_res.CO3zt(:,end), ...
sed_res.HCO3zt(:,end), ...
sed_res.H2CO3zt(:,end)], 'delimiter', '\t', '-append')
